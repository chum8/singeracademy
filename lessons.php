<span class="emphasize_and_center" id="philosophy"><br />Studio Philosophy</span><br />
Why does music touch your emotions more than words, images or sensations?<br />
Why does music inform and shape entire cultures?<br />
Why do people with no music education whatsoever have the ability to create beautiful, inspiring and timeless works of music?<br /><br />

All of us have a musical gift within our souls.  For some, this musical gift will not necessarily result in highly trained, skillful performance on an instrument.  But nevertheless, you would be hard-pressed to find someone who simply hates music.  <br /><br />
<span class="italicize">
Everyone has a favorite kind of music, because everyone, in the core of their being, has some degree of musical talent.<br /><br />
</span>
I believe that music is the highest art form, and one of God's most generous gifts to mankind.  And yet, in spite of the fact that the creation of music is one of the most enthralling, exciting and divine of all experiences, many students tire of piano lessons after a year or two.<br /><br />

Why?<br /><br />

Students get bored quickly because the True Art of making music is often neglected, and sometimes lost.  <br /><br />

Without a consistent application of the True Art of music, piano lessons become nothing more than learning to read music and make the right keys go down at the right time.<br /><br />

What is the True Art of making music?<br /><br />

It is so simple that each of us have access to it.  And so neglected that many technically skilled musicians forget about it.  <br /><br />

The True Art of making music is communication.  Music is the incredible art that expresses more than words can say, and yet, its connection with the spoken word, with the theatrical stage, with human actions, and with human intentions, is very important.  For therein is the watershed of all musical performances:  on the one hand, music that touches us, speaks to us, and draws us in, and on the other hand, music that sounds like, well, notes.  If you wish to learn the True Art of making music, please apply to study with me today!  Applications from students of all ages and levels are being accepted.  <br /><br />
