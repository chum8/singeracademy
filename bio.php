Mr. Douglas Michael Singer is currently the pianist for the Hastings Symphony Orchestra, a freelance piano teacher at Moore Music in Hastings, NE, and the Executive Producer of <a href="http://www.singteam.com">SingTeam America</a>.<br /><br />
Mr. Singer has enjoyed playing piano for 22 years and has taught for 10 years.  A native of Colorado, Mr. Singer received degrees in music from Gardner-Webb University (B.M., 2004) and the University of Northern Colorado (M.M., 2006).  
<br/><br/>
Mr. Singer taught piano and accompanied piano in Indiana for 4 years, serving as an adjunct piano professor at Taylor University, Ranked #1 Baccalaureate College in the Midwest for 5 years running.  He appeared as a faculty soloist, feature artist, and ensemble pianist at various Taylor University events, and gave a joint concert in conjunction with mezzo-soprano Jennifer Arbogast.  He played jazz piano with the Jazz Rolle Sextet.   He formed a jazz combo and directed jazz vocalists for the Taylor Sounds Valentine's Banquet.<br /><br />
Mr. Singer was commissioned by Shakespeare Playworks, a homeschool organization in Muncie, IN, to write original music for their 2012 production of "As You Like It."  Mr. Singer became the Music Director for the production, recruiting a rock n' roll band and directing the vocalists for the production.  Mr. Singer will be guest to Shakespeare Playworks again in 2013 as Visiting Composer and Music Director.
<br /><br />
Mr. Singer recently appeared with "Meet the Composers" in Sterling, Colorado.<br/><br/>
As a former collaborative pianist in the studios of Mei Zhong of Ball State University, Patricia Robertson of Taylor University, Shederick Whipple of Taylor University, and others, Mr. Singer accompanied numerous award winning vocalists.  He accompanied a three concert series by mezzo-soprano Loralee Songer, including a presentation at the College Music Society Great Lakes Regional Conference.  He accompanied the winning performances of two NATS Advanced Division First Place vocalists, the Canadian soprano Julie Lyn Barber and the Korean baritone Yeong-June Choi.  He accompanied other award-winning performances at Indiana NATS and MTNA.   He accompanied and coached numerous students at Taylor University, and served as accompanist for Taylor Theater, Taylor Sounds, Taylor Chorale, Taylor Opera, and Taylor Adoration Chorus.  He played piano for the Farmland Dinner Theater performance of "Clue: The Musical."  
<br /><br />
Mr. Singer appeared with the Marion Philharmonic Orchestra as Keyboard II player during the May 2010 performance of Stravinsky's "Symphony of the Psalms" under the baton of Alex Platt.  He appeared as piano soloist during the world premiere of his original work, "Judah," premiered by Patricia Sparti and the Gardner-Webb University Community Orchestra.
<br /><br />
Douglas Singer is married to long-time Hastings resident and Hastings College graduate, Laura Singer (nee Demick).  They have two beautiful children.  <br /><br />
